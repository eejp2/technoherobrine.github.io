function openImage1(){
	document.getElementById("scrsht1").style.visibility = "visible";
	document.getElementById("scrsht1").style.display = "block";

	document.getElementById("darkfullscreen").style.visibility = "visible";
	document.getElementById("darkfullscreen").style.display = "block";
}
function closeImage1(){
	document.getElementById("scrsht1").style.visibility = "hidden";
	document.getElementById("scrsht1").style.display = "none";

	document.getElementById("darkfullscreen").style.visibility = "hidden";
	document.getElementById("darkfullscreen").style.display = "none";
}
